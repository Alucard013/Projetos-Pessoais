function mostrar(){
    box_cadastro.style.display= 'none';
    box_login.style.display= 'block';
    a_cadastro.style.display= 'block';
    a_login.style.display= 'none';
}

function revelar(){
    a_cadastro.style.display= 'block';
    a_login.style.display= 'none';
    box_cadastro.style.display= 'block';
    box_login.style.display= 'none';
}